#include "checker.h"

void	ft_is_dig(char *s, t_list **fst_lst)
{
	if ((*s) == '-')
		s++;
	while (*s)
	{
		if (*s < 48 || *s > 57)
		{
			ft_lstclear(fst_lst);
			ft_exit();
		}
		s++;
	}
}
