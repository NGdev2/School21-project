#include "../checker.h"

void	ft_rb(t_list **lst_b)
{
	t_list	*el;

	if (*lst_b && (*lst_b)->next)
	{
		el = (*lst_b)->next;
		(*lst_b)->next = NULL;
		ft_lstadd_back(&el, *lst_b);
		*lst_b = el;
	}
}
